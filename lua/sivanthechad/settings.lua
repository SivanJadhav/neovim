-- General Settings
vim.opt.number = true -- Shows line numbers
vim.opt.relativenumber = true -- Shows relative line numbers, i.e. how far away is a line relative to the current one
vim.opt.shiftwidth = 4 -- Indent width
vim.opt.tabstop = 4 -- Tab character width
vim.opt.smartindent = true -- Auto-indent new lines
vim.opt.clipboard = "unnamedplus" -- Use system clipboard
vim.opt.hlsearch = true -- Highlight search results
vim.opt.incsearch = true -- Incremental search
vim.opt.termguicolors = true -- True color support
vim.opt.smartcase = true -- Searches based on smartcase, i.e. respects case if capslock otherwise doesn't
